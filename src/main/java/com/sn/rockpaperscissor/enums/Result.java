package com.sn.rockpaperscissor.enums;

/**
 * Define the possible result values.
 * <ul>
 * <li>{@link #LOSE}</li>
 * <li>{@link #TIE}</li>
 * <li>{@link #WIN}</li>
 * </ul>
 */
public enum Result {

    /**
     * the shape is weaker.
     */
    LOSE,

    /**
     * the same shape.
     */
    TIE,

    /**
     * The shape is stronger.
     */
    WIN
}
